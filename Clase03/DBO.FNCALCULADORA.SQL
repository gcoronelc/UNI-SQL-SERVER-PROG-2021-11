CREATE FUNCTION DBO.FNCALCULADORA( @OPERADOR CHAR(1), @NUMERO1 INT, @NUMERO2 INT )
RETURNS INT
AS
BEGIN
	-- VARIABLES
	DECLARE @RESPUESTA INT;
	-- PROCESO
	SET @RESPUESTA = CASE 
		WHEN @OPERADOR='+' THEN @NUMERO1 + @NUMERO2
		WHEN @OPERADOR='-' THEN @NUMERO1 - @NUMERO2
		WHEN @OPERADOR='*' THEN @NUMERO1 * @NUMERO2
		WHEN @OPERADOR='/' THEN @NUMERO1 / @NUMERO2
		ELSE 0
	END;
	-- REPORTE
	RETURN @RESPUESTA;
END;
GO


SELECT DBO.FNCALCULADORA('/', 56, 5) RESPUESTA;
